
// Conditional statements is code waarmee je het verloop van je programma bepaalt.
// Dit wordt gedaan met de if/else

// Een voorbeeld met alleen if:
if( 6 > 4) {
    console.log("6 is inderdaad groter dan 4");
}

// Een ander voorbeeld:
let naam = "Neymar"
if(naam === "Neymar") {
    console.log("Resultaat is 'true', want naam is inderdaad Neymar");
}

// Een voorbeeld met if en else:
if( 4 > 6) {
    console.log("6 is inderdaad groter dan 4");
} else {
    console.log("Fout! 4 is niet groter dan 6!");
}

